#include <iostream>

using namespace std;

int main()
{
	int correr = 20;		// incremento pré-fixado
	++correr;
	
	int andar = 30;			// decremento pré-fixado
	--andar;
	
	int a = 1;
	int b = 2;
	int c = 3;
	int d = 4;
	int e = 5;
	
	a += 1;					// atribuição soma
	b -= 1;					// atribuição subtração
	c *= 1;					// atribuição multiplicação
	d /= 1;					// atribuição divisão
	e %= 1;					// atribuição resto da divisão
	
}
