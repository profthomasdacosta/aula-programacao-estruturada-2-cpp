#include <iostream>
#include "matematica.h"

using namespace std;

namespace matematica
{
	double somar(double x, double y)
	{
		return x + y;
	}
	
	double subtrair(double x, double y)
	{
		return x - y;
	}
	
	double multiplicar(double x, double y)
	{
		return x * y;
	}
	
	double dividir(double x, double y)
	{
		return x / y;
	}
}
